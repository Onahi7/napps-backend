/**
 * Generate PDF Receipt for a completed payment
 * Uses the API to fetch payment data and generates a simple text-based receipt
 */

const fs = require('fs');
const path = require('path');

const PAYMENT_REFERENCE = process.argv[2] || 'LEVY_1771853902234_146553';
const API_BASE_URL = 'http://localhost:3001/api/v1';

async function fetchPaymentData() {
  console.log('📡 Fetching payment data...\n');
  
  const response = await fetch(`${API_BASE_URL}/levy-payments/${PAYMENT_REFERENCE}`);
  
  if (!response.ok) {
    throw new Error(`Failed to fetch payment: ${response.status}`);
  }
  
  return await response.json();
}

function generateTextReceipt(payment) {
  const width = 80;
  const line = '='.repeat(width);
  const dashed = '-'.repeat(width);
  
  const receipt = `
${line}
                    NAPPS NASARAWA STATE BRANCH
           National Association of Proprietors of Private Schools
                         PAYMENT RECEIPT
${line}

Receipt Number: ${payment.receiptNumber}
Reference: ${payment.reference}
Date: ${new Date(payment.paidAt).toLocaleString('en-NG', {
    year: 'numeric',
    month: 'long',
    day: 'numeric',
    hour: '2-digit',
    minute: '2-digit'
  })}
Status: ${payment.status.toUpperCase()} ✓

${dashed}
PAYER INFORMATION
${dashed}

Name:           ${payment.memberName}
Email:          ${payment.email}
Phone:          ${payment.phone}
Chapter:        ${payment.chapter}

${dashed}
SCHOOL INFORMATION
${dashed}

School Name:    ${payment.schoolName}
Wards:          ${payment.wards.join(', ')}

${dashed}
PAYMENT DETAILS
${dashed}

Description                                              Amount
${dashed}
NAPPS NASARAWA STATE SECRETARIAT BUILDING LEVY      ₦${(payment.amount / 100).toLocaleString()}

${dashed}
TOTAL AMOUNT PAID                                   ₦${(payment.amount / 100).toLocaleString()}
${line}

Payment Method: ${payment.paymentMethod ? payment.paymentMethod.toUpperCase() : 'CARD'}
Transaction ID: ${payment.flutterwaveTransactionId || 'N/A'}

${dashed}

                    Thank you for your payment!

This is an official payment receipt generated by NAPPS Nasarawa State.
For inquiries, please contact: info@nappsnasarawa.com

Generated on: ${new Date().toLocaleString('en-NG')}

${line}
`;

  return receipt;
}

